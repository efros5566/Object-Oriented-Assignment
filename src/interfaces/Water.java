package interfaces;

/**
 * The Water interface represents animals that live in water.
 */
public interface Water {

    /**
     * Checks if the animal has gills.
     *
     * return true if the animal has gills, false otherwise.
     */
    boolean hasGills();

    /**
     * Checks if the animal lays eggs in water.
     * return true if the animal lays eggs in water, false otherwise.
     */
    boolean hasLaysEggs();
}
